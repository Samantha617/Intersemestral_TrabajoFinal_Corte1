    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String fecha = txtFechaVacuna.getText();
        String lote = txtLote.getText().trim();
        String nombreM = txtMascota.getText();
        String tipoVacuna = (String) cbTipoVacuna.getSelectedItem();
        String proximaDosis = txtProximaDosis.getText();

        // Validar campos vacios
        if (fecha.isEmpty() || lote.isEmpty() || nombreM.isEmpty() || tipoVacuna == null || proximaDosis.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Buscar la mascota asociada recorriendo todos los propietarios
        Mascota mascota = null;
        for (Propietario propi : Propietario.listaPropietarios) {
            for (Mascota masco : propi.getListaMascotas()) {
                if (masco.getNombreM().equalsIgnoreCase(nombreM)) {
                    mascota = masco;
                    break;
                }
            }
            if (mascota != null) {
                break;
            }
        }

        // Validar si se encontro la mascota
        if (mascota == null) {
            JOptionPane.showMessageDialog(this, "La mascota no esta registrada.... Registrala primero",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Crear el objeto Vacuna
        Vacuna vacuna = new Vacuna(fecha, lote, mascota, tipoVacuna, proximaDosis);

        // Guardar la vacuna en la lista
        Vacuna.listaVacunas.add(vacuna);

        // Mensaje de confirmaci√≥n
        JOptionPane.showMessageDialog(this,
                "---------Vacuna registrada:-----------\n"
                + "Fecha: " + fecha + "\n"
                + "Lote: " + lote + "\n"
                + "Mascota: " + mascota.getNombreM() + "\n"
                + "Tipo: " + tipoVacuna + "\n"
                + "Proxima dosis: " + proximaDosis);

        // Limpiar campos y actualizar tabla
        limpiarCampos();
        llenarTabla();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String fecha = txtFechaVacuna.getText().trim();

        // Validar si el campo de fecha esta vacio
        if (fecha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese la fecha de la vacuna a buscar");
            return;
        }

        // Buscar vacuna en la lista
        Vacuna vacunaEncontrada = null;
        for (Vacuna v : Vacuna.listaVacunas) {
            if (v.getFechaVacuna().equalsIgnoreCase(fecha)) {
                vacunaEncontrada = v;
                break;
            }
        }

        // Verificar si se encontro
        if (vacunaEncontrada != null) {
            // Mostrar los datos en el formulario
            txtLote.setText(vacunaEncontrada.getLote());
            txtMascota.setText(vacunaEncontrada.getMascota().getNombreM());
            cbTipoVacuna.setSelectedItem(vacunaEncontrada.getTipoDeVacuna());
            txtProximaDosis.setText(vacunaEncontrada.getProximaDosis());

            JOptionPane.showMessageDialog(this, "Vacuna encontrada");
        } else {
            JOptionPane.showMessageDialog(this, "No se encontro ninguna vacuna con esa fecha");
        }                
    }//GEN-LAST:event_btnBuscarActionPerformed

