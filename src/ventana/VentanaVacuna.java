//-----------creo metodo limpiar Campos----------------------
    private void limpiarCampos() {
        txtFechaVacuna.setText("");
        txtLote.setText("");
        txtMascota.setText("");
        cbTipoVacuna.setSelectedIndex(0); //selecciona la primera opcion
        txtProximaDosis.setText("");
    }
//------------creo metodo de llenar tabla------------------------

    private void llenarTabla() {

        DefaultTableModel modelo = (DefaultTableModel) tblVacunas.getModel();
        modelo.setRowCount(0); // Limpia la tabla antes de llenarla

        for (Vacuna vacuna : Vacuna.listaVacunas) {
            modelo.addRow(new Object[]{
                vacuna.getFechaVacuna(),
                vacuna.getLote(),
                vacuna.getMascota().getNombreM(),
                vacuna.getTipoDeVacuna(),
                vacuna.getProximaDosis()
            });
        }
    }

